    private void preencheTabela() {
        //Criar uma var "cursoDAO"
        //pegar os dados dos cursos da lista e jogar na tabela
        AlunoDAO alunoDAO = new AlunoDAO();
        List<Aluno> listaAlunos = alunoDAO.getAlunos();

        DefaultTableModel tabelaAlunos = (DefaultTableModel) tblAlunos.getModel();
        //limpar a tabela para preencher com os novos dados
        tabelaAlunos.setNumRows(0);
        //percorrer o "listaAlunso" e inserir na tabelaCursos
        for (Aluno a : listaAlunos) {
            Object[] obj = new Object[]{
                a.getId(),
                a.getNomealuno(),
                a.getCursoid().getNomecurso()
            };
            tabelaAlunos.addRow(obj);
        }
    }
